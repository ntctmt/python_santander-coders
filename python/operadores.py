# OPERADORES MATEMÁTICOS

"""
soma: +
subtração: -
multiplicação: *
divisão: /
divisão inteira: // (descarta a parte decimal)
resto da divisão: % (chama-se mod)
potência: **
"""

numero1 = 10
numero2 = 20

print (numero1 + numero2)
print (numero1 - numero2)
print (numero1 * numero2)
print (numero1 / numero2)
print (numero1 // numero2)
print (20 % 3)
print (2 ** 3)

# OPERADORES BOOLEANOS

idade1 = 10
idade2 = 15
altura1 = 1.70
altura2 = 1.65
altura3 = altura2

print (idade1 > idade2)
# > maior

print (idade1 <= idade2)
# <= menor ou igual

print ("Python" == "python")
# == comparar

print ("Banana" != "Abacaxi")
# != diferente

print (altura1 >= altura2)
# >= maior ou igual

print (altura2 > altura3)
# > maior